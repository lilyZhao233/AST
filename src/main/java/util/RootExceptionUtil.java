    public static boolean hasRelation(String exception1, String exception2){
        build();
        DefaultMutableTreeNode node=Throwable;
        while (node!=null){
            if(String.valueOf(node).equals(exception1)&&hasNode(node,exception2)) {
                return true;
            }
            node=node.getNextNode();
        }
        return false;
    }

    public static boolean hasNode(DefaultMutableTreeNode tree,String exception){
        if(tree==null){
            return false;
        }else if(String.valueOf(tree).equals(exception)) {
            return true;
        } else{
            for(int i=0;i<tree.getChildCount();i++){
                if(hasNode((DefaultMutableTreeNode) tree.getChildAt(i),exception)){
                    return true;
                }
            }
        }
        return false;
    }
